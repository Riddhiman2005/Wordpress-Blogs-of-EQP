<figure class="wp-block-image size-large is-resized"><img src="https://everlastingquestforphysics.files.wordpress.com/2023/08/image-21.png?w=289" alt="" class="wp-image-1102" style="width:683px;height:179px" width="683" height="179" /></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p><br>In field theory, a shift from working with Lagrangians to working with Lagrangian densities, denoted as <strong>L</strong>, occurs and is defined as</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":1098,"width":664,"height":66,"sizeSlug":"large","linkDestination":"none"} -->
<figure class="wp-block-image size-large is-resized"><img src="https://everlastingquestforphysics.files.wordpress.com/2023/08/lagrida_latex_editor-21.png?w=145" alt="" class="wp-image-1098" style="width:664px;height:66px" width="664" height="66" /></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>This shift allows the expression of action in terms of spacetime variables. The Lagrangian density involves a generalized field <strong>η</strong>, its derivative <strong>∂<sub>νη</sub></strong>, and spacetime variables, <strong>x<sup>ν</sup></strong>. The principle of least action dictates that the action is extremized</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":1095,"width":664,"height":66,"sizeSlug":"large","linkDestination":"none"} -->
<figure class="wp-block-image size-large is-resized"><img src="https://everlastingquestforphysics.files.wordpress.com/2023/08/lagrida_latex_editor-19-1.png?w=415" alt="" class="wp-image-1095" style="width:664px;height:66px" width="664" height="66" /></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p> For any path, whether extremal or not, the endpoints remain fixed. This constraint leads to variations being zero on the <strong>d-1</strong> dimensional surface that bounds the <strong>d</strong>-dimensional integration volume. Considering a path parameterized by α, we find that</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":1096,"width":664,"height":39,"sizeSlug":"large","linkDestination":"none"} -->
<figure class="wp-block-image size-large is-resized"><img src="https://everlastingquestforphysics.files.wordpress.com/2023/08/lagrida_latex_editor-20-1.png?w=296" alt="" class="wp-image-1096" style="width:664px;height:39px" width="664" height="39" /></figure>
<!-- /wp:image -->

<!-- wp:paragraph {"fontSize":"medium"} -->
<p class="has-medium-font-size"> where, ξ vanishes at the endpoints. Because the action is extremal for <strong><mark style="background-color:rgba(0, 0, 0, 0);color:#346b67" class="has-inline-color">η(x<sup>ν</sup>;0)</mark></strong>, its derivative with respect to α must also vanish.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":1062,"width":664,"height":111,"sizeSlug":"large","linkDestination":"none"} -->
<figure class="wp-block-image size-large is-resized"><img src="https://everlastingquestforphysics.files.wordpress.com/2023/08/lagrida_latex_editor-2-3.png?w=426" alt="" class="wp-image-1062" style="width:664px;height:111px" width="664" height="111" /></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>Integrating by parts,</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":1071,"sizeSlug":"large","linkDestination":"none"} -->
<figure class="wp-block-image size-large"><img src="https://everlastingquestforphysics.files.wordpress.com/2023/08/lagrida_latex_editor-12-1.png?w=669" alt="" class="wp-image-1071" /></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>The reason the second term disappears is that we convert the divergence theorem in (d)-dimensional space into an integration over the surface that encloses the integration region. This integration yields zero because the variation of η is 0 on the surface.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":1064,"width":661,"height":123,"sizeSlug":"large","linkDestination":"none"} -->
<figure class="wp-block-image size-large is-resized"><img src="https://everlastingquestforphysics.files.wordpress.com/2023/08/lagrida_latex_editor-5-3.png?w=382" alt="" class="wp-image-1064" style="width:661px;height:123px" width="661" height="123" /></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>This is the Euler-Lagrange equation for fields.</p>
