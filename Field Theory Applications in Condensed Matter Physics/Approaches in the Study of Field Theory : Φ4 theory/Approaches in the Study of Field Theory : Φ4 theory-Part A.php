<h4 class="wp-block-heading has-x-large-font-size" style="font-style:normal;font-weight:400"><strong><span style="text-decoration: underline">Local Gauge Invariance</span></strong></h4>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p><br>From any given Lagrangian density, we can extract its corresponding field equation. In the context of the free scalar theory, our Lagrangian density takes the form </p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":1121,"sizeSlug":"large","linkDestination":"none"} -->
<figure class="wp-block-image size-large"><img src="https://everlastingquestforphysics.files.wordpress.com/2023/08/lagrida_latex_editor-11-2.png?w=227" alt="" class="wp-image-1121" /></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>. In this equation, the symbol <strong>Φ</strong> represents the field itself, while the parameter <strong>m</strong> holds the interpretation as the mass associated with the particle.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":1120,"width":664,"height":134,"sizeSlug":"large","linkDestination":"none"} -->
<figure class="wp-block-image size-large is-resized"><img src="https://everlastingquestforphysics.files.wordpress.com/2023/08/lagrida_latex_editor-10-3.png?w=453" alt="" class="wp-image-1120" style="width:664px;height:134px" width="664" height="134" /></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p> In particle physics, this is the <strong>Klein Gordon equation</strong> for massive spin 0 particles.</p>
