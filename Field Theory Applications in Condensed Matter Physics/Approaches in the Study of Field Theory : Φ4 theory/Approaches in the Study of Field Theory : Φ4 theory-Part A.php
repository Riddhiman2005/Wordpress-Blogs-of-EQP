<h4 class="wp-block-heading has-large-font-size" style="font-style:normal;font-weight:400"><span style="text-decoration: underline"><strong>Classical Field Theory</strong></span></h4>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p><br>In the realm of physics, fields play a fundamental role by describing continuous quantities across a manifold. These fields can be real or imaginary and take forms such as scalars, vectors, or tensors, depending on the point in the manifold. The manifold itself can take different geometries, such as Euclidean or Minkowski spacetime. The extension of fields to Quantum Field Theory (QFT) is natural, given that spacetime is believed to be continuous according to current understanding. The Langrangian formulation of mechanics smoothly transitions to these continuous fields.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":4,"style":{"typography":{"fontStyle":"normal","fontWeight":"400"}},"fontSize":"large"} -->
<h4 class="wp-block-heading has-large-font-size" style="font-style:normal;font-weight:400"><strong><span style="text-decoration: underline">Principle of Least Action</span></strong></h4>
