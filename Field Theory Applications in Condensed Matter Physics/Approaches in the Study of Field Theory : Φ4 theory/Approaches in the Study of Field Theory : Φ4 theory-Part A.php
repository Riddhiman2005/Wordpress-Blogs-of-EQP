<!-- wp:paragraph -->
<p>A gauge theory is a type of field theory where the behavior of the system remains constant when subjected to a specific symmetry change linked to a Lie group. To illustrate, let's begin by considering the consistency of the Lagrangian density for a set of n real-valued scalar fields.</p>
