<h4 class="wp-block-heading" style="font-style:normal;font-weight:400"><strong><span style="text-decoration: underline">Feynman Diagrams and Correlation Functions</span></strong></h4>
<!-- /wp:heading -->

<!-- wp:paragraph {"style":{"elements":{"link":{"color":{"text":"var:preset|color|vivid-red"}}}}} -->
<p class="has-link-color"><br>This portion is based on a Recent Very Interesting Paper : <a href="https://arxiv.org/pdf/2301.13234.pdf">Field Theoretic Aspects of Condensed Matter Physics: An  Overview</a> by Eduardo Franklin  </p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Quantum field theory has played an important role in establishing the <strong>Fermi Liquid theory</strong> which was developed by <strong>Pines, Nozières (1966)</strong>, <strong>Baym and Pethick (1991)</strong>. Initially, Landau introduced the Fermi liquid theory using hydrodynamics and the quantum Boltzmann equation. Later, Abrikosov and his group provided a microscopic foundation using Green functions and Feynman diagrams, considering quantum vacuum fluctuations at finite temperature and non-equilibrium situations. Linear response theory emerged, enabling the calculation of response functions like electrical conductivities and magnetic susceptibilities based on correlation functions derived from microscopic theories, with these functions computed using Feynman diagrams.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>These ideas heavily borrowed from field theory, including the study of generalized susceptibilities, spectral functions, and dispersion relations, leading to the fluctuation-dissipation theorem. These concepts found wide application in metals , superconductors and quantum magnets.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In interacting systems, low-energy excitations called quasiparticles emerge, characterized by quantum numbers related to the theory's symmetries. <strong>Quasiparticles</strong>, like the "dressed" electron in <strong>Landau's Fermi liquid theory</strong>, retain the charge and spin of an electron but possess a renormalized effective mass. These quasiparticles have a crucial role in condensed matter physics, with their spectrum evident from the poles of correlation functions.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The role of symmetries, especially gauge invariance, in correlation functions was extensively explored. Symmetries entail Ward identities that link various correlation functions. In theories with globally conserved charges, like a <strong>global U(1) symmetry</strong> associated with transformations of field operators, Ward identities require correlations to be transverse. Global symmetries can become gauge symmetries in the absence of quantum anomalies.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>Spontaneous breaking of global symmetries can lead to gapless excitations known as Goldstone bosons</strong>. For instance, superfluids with a broken <strong>U(1) symmetry have gapless phase mode</strong>s, while the<strong> Néel phase of a quantum antiferromagnet exhibits two gapless Goldstone bosons due to the broken SO(3) symmetry</strong>. Quantum electrodynamics' Ward identity connects electron self-energy and electron-photon vertex functions, applicable even in non-relativistic electron fluids. These concepts also imply sum rules for spectral functions.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Overall, these insights from quantum field theory have become essential tools for analyzing experimental data and making predictions in condensed matter physics. Ward identities and sum rules help determine allowed approximations when predicting outcomes from microscopic models.</p>
