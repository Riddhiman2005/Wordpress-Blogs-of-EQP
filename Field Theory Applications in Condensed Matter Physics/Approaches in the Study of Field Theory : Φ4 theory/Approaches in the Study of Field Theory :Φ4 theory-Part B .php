<h4 class="wp-block-heading" style="font-style:normal;font-weight:400"><strong><span style="text-decoration: underline">Path Integral Formulation</span></strong></h4>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p><em><strong>The path integral formulation in Quantum Mechanics (QM) and Quantum Field Theory (QFT) extends the classical mechanics' principle of action</strong></em>. It replaces the classical concept of a single, definite trajectory for a system with a summation, or a functional integral, over an infinite range of quantum mechanically possible trajectories.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>When computing a quantum amplitude, the path integral approach replaces the classical notion of a single trajectory with a sum over an infinite number of possible trajectories, reflecting the inherent uncertainty of quantum systems. This method is applied to scalar field theory by first deriving the formulation for a quantum mechanical phase space and then extending it to fields. The path integral concept allows us to calculate the probability of a transition between two points in the phase space.</p>
