
Field Theory Applications in Condensed Matter Physics-III




<!-- wp:paragraph -->
<p>Hi Everyone, this is the fifth and the last post of the <a href="https://everlastingquestforphysics.wordpress.com/2023/08/14/field-theory-applications-in-condensed-matter-physics/">series</a> where we'll discuss some other Statistical Field Theories. In this post,  we'll see  other statistical systems using fields. It's a very vast area but I'll only study Fermi gases and Goldstone bosons. </p>
