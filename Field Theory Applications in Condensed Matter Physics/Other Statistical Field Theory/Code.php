<h4 class="wp-block-heading" style="font-style:normal;font-weight:400"><strong><span style="text-decoration: underline">Goldstone Bosons</span></strong></h4>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p><br>The different phases of matter can be distinguished by two distinct symmetry groups  G that pertains to the symmetry of the system's free energy, and H that corresponds to the symmetry of the ground state.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":1324,"sizeSlug":"large","linkDestination":"none"} -->
<figure class="wp-block-image size-large"><img src="https://everlastingquestforphysics.files.wordpress.com/2023/08/image-26.png?w=1024" alt="" class="wp-image-1324" /></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>This structure is evident in the Ising model. When B=0, the free energy exhibits a <strong>Z</strong><sub>2</sub> symmetry. In the disordered phase, this symmetry remains unaltered—both the Hamiltonian H and the system possess a <strong>Z</strong><sub>2</sub> symmetry. However, in the ordered phase, the symmetry is spontaneously broken as the system selects one of two possible ground states. In this scenario, the Hamiltonian H does not possess any symmetry group.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>When a discrete symmetry group undergoes spontaneous breaking, it gives rise to several distinct ground states. Transitioning from one ground state to another becomes possible by applying the generators of G that have been broken. The various phases of matter falling under this category are defined by the nature of H. Consequently, a phase transition occurs whenever there is a change in H.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The concept of spontaneous symmetry breaking in condensed matter systems can be illustrated using the theory of N real scalar fields featuring an O(N) symmetry. Specifically, models with N=2 fall into the XY-universality class. This framework effectively describes phenomena such as <strong>Bose-Einstein condensates</strong> and superfluids.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Pairing the two real fields into a single complex field <strong>ψ =φ<sub>1</sub>+iφ<sub>2</sub></strong> proves to be a convenient approach. This pairing maintains invariance under U(1) transformations, where ψ' remains unchanged when subjected to e<sup>iθ</sup>ψ . Unlike the Ising model with only two potential ground states, continuous symmetries introduce an array of infinitely many ground states.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The minimum of the free energy imposes a constraint solely on the magnitude of ψ, resulting in </p>
<!-- /wp:paragraph -->

